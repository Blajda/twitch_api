use std::marker::PhantomData;
pub use super::models;
pub use super::Client;

pub mod clips;
pub mod users;

pub struct Namespace<T> {
    client: Client,
    _type: PhantomData<T>
}

impl<T> Namespace<T> {
    pub fn new(client: &Client) -> Self {
        Namespace {
            client: client.clone(),
            _type: PhantomData,
        }
    }
}
